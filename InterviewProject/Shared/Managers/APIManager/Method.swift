import Foundation

/// Enum representing HTTP methods.
///
/// - GET: HTTP GET method.
/// - POST: HTTP POST method.
/// - PUT: HTTP PUT method.
/// - DELETE: HTTP DELETE method.
enum Method: String {
    case GET
    case POST
    case PUT
    case DELETE
}
